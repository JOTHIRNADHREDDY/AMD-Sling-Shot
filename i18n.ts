/**
 * Get a translated label by key and language, with automatic English fallback.
 *
 * Usage:
 *   getLabel('welcome', Language.TELUGU)      → 'స్వాగతం'
 *   getLabel('missingKey', Language.HINDI)     → '' (empty string, key not found)
 *   getLabel('welcome', Language.TELUGU_EN)    → 'Swagatam'
 *
 * @param key   The translation key (e.g. 'welcome', 'queueTitle')
 * @param lang  The target language
 * @returns     Translated string, or English fallback, or empty string
 */
export function getLabel(key: string, lang: Language): string {
  // Try the requested language first
  const langLabels = translations[lang];
  if (langLabels && langLabels[key]) {
    return langLabels[key];
  }

  // Fallback to English
  const englishLabels = translations[Language.ENGLISH];
  if (englishLabels && englishLabels[key]) {
    return englishLabels[key];
  }

  // Key not found in any language
  return '';
}

/**
 * Shorthand hook to get the full translations object.
 * Actual usage requires the language from KioskContext.
 */
export function useTranslation() {
  return translations;
}
